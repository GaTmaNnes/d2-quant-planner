import numpy as np
from ortools.sat.python import cp_model
from typing import List, Dict

# =========================
# HARDWARE MODEL (portable)
# =========================
BW = 50e9              # memory bandwidth (GB/s approx scaled)
FLOPS = 30e12          # generic GPU compute proxy
L2 = 48e6              # L2 cache bytes
DTYPE_BYTES = {"FP16":2, "INT8":1, "INT4":0.5}

TPS_GAIN = {"FP16":1.0, "INT8":1.35, "INT4":1.75}
RISK = {"FP16":0.2, "INT8":0.6, "INT4":1.2}

# =========================
# MODEL FORMAT
# =========================
def default_model():
    return [
        {"name":"attn_q", "size":4096*4096},
        {"name":"attn_k", "size":4096*4096},
        {"name":"attn_v", "size":4096*4096},
        {"name":"ffn_up", "size":4096*14336},
        {"name":"ffn_down", "size":14336*4096},
        {"name":"lm_head", "size":4096*32000},
    ]

# =========================
# ROOFLINE MODEL (simple but correct)
# =========================
def roofline_cycles(size_bytes, dtype):
    bw_time = size_bytes / BW
    comp_time = size_bytes / FLOPS
    return max(bw_time, comp_time)

# =========================
# VRAM MODEL
# =========================
def vram_cost(layers, assign):
    total = 0
    for l, q in zip(layers, assign):
        total += l["size"] * DTYPE_BYTES[q]
    return total / 1e9  # GB

# =========================
# ILP SOLVER
# =========================
def solve_quantization(layers, vram_budget_gb=4.0):
    model = cp_model.CpModel()

    Q = ["FP16","INT8","INT4"]

    x = {}
    for i in range(len(layers)):
        for q in Q:
            x[i,q] = model.NewBoolVar(f"x_{i}_{q}")

    # one precision per layer
    for i in range(len(layers)):
        model.Add(sum(x[i,q] for q in Q) == 1)

    # VRAM constraint
    vram_expr = []
    for i,l in enumerate(layers):
        for q in Q:
            vram_expr.append(x[i,q] * int(l["size"] * DTYPE_BYTES[q]))

    model.Add(sum(vram_expr) <= int(vram_budget_gb * 1e9))

    # =========================
    # OBJECTIVE (stable version)
    # =========================
    objective = []

    for i,l in enumerate(layers):
        for q in Q:

            size = l["size"]

            tps = TPS_GAIN[q] * size * 1e-12
            risk = RISK[q] * np.log1p(size)

            score = int(1000 * (tps - 0.25 * risk))

            objective.append(score * x[i,q])

    model.Maximize(sum(objective))

    solver = cp_model.CpSolver()
    solver.parameters.max_time_in_seconds = 3.0

    status = solver.Solve(model)

    if status not in (cp_model.OPTIMAL, cp_model.FEASIBLE):
        return None

    plan = []
    for i,l in enumerate(layers):
        for q in Q:
            if solver.Value(x[i,q]):
                plan.append((l["name"], q))

    return plan

# =========================
# GGUF EXPORT (llama.cpp style)
# =========================
def export_gguf(plan):
    out = {
        "backend":"llama.cpp",
        "quantization_map":[
            {"layer":name, "dtype":dtype}
            for name,dtype in plan
        ],
        "kv_cache":"fp16",
        "notes":"generated by V16 planner"
    }
    return out

